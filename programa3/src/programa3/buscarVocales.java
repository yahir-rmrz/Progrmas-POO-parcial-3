    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
            Operacion op = new Operacion();
            String cad = txtPalabra.getText();
        if (optA.isSelected()){
            int c = op.BuscarA(cad);
            lblLetra.setText("El numero de A es: ");
            lblCantidad.setText(Integer.toString(c));
        }
        if (optE.isSelected()){
            int c = op.BuscarE(cad);
            lblLetra.setText("El numero de E es: ");
            lblCantidad.setText(Integer.toString(c));
        }
        if (optI.isSelected()){
            int c = op.BuscarI(cad);
            lblLetra.setText("El numero de I es: ");
            lblCantidad.setText(Integer.toString(c));
        }
        if (optO.isSelected()){
            int c = op.BuscarO(cad);
            lblLetra.setText("El numero de O es: ");
            lblCantidad.setText(Integer.toString(c));
        }
        if (optU.isSelected()){
            int c = op.BuscarU(cad);
            lblLetra.setText("El numero de U es: ");
            lblCantidad.setText(Integer.toString(c));
        }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

